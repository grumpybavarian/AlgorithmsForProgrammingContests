#include <iostream>
#include <vector>
#include <algorithm>
#include <cmath>
using namespace std;

struct Point {
    double x,y;
};

int main() {
    cout.precision(10);
    int t;
    cin >> t;
    for (int ti=1; ti<=t; ++ti) {
        int n;
        cin >> n;
        double sumCP = 0;
        vector<Point> p(n);
        for (int i=0; i<n; ++i) {
            double a,b;
            cin >> a >> b;
            p[i] = (Point {a,b});
        }
        for (int i=0; i<n-1; ++i) {
            sumCP += (p[i].x+p[i+1].x) * (p[i].y-p[i+1].y);
        }
        sumCP += (p[n-1].x+p[0].x) * (p[n-1].y-p[0].y);
        sumCP = fabs(sumCP);
        cout << "Case #" << ti << ": " << 0.5*sumCP << "\n";
    }
    return 0;
}

